/// <summary>
/// An animation metadata record.
/// </summary>
public class AnimationRecord : Record
{
    /// <summary>
    /// The duration of the animation.
    /// </summary>
    public float duration;
    /// <summary>
    /// If true, the animation loops.
    /// </summary>
    public bool loop;
    /// <summary>
    /// The animation framerate.
    /// </summary>
    public int framerate;
}